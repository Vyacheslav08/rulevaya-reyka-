export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*')
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS')
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type')

  if (req.method === 'OPTIONS') return res.status(200).end()
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' })

  const { question, inventory } = req.body

  if (!question) return res.status(400).json({ error: 'question is required' })

  const inventoryContext = inventory
    ? `\n\nАКТУАЛЬНЫЙ ОСТАТОК НА СКЛАДЕ (из Google Sheets, сейчас):\n${inventory}`
    : ''

  try {
    const message = await client.messages.create({
      model: 'claude-sonnet-4-6',
      max_tokens: 600,
      system: SYSTEM_PROMPT + inventoryContext,
      messages: [{ role: 'user', content: question }],
    })

    res.json({ answer: message.content[0].text })
  } catch (err) {
    console.error('Claude API error:', err)
    res.status(500).json({ error: 'Claude API error', details: err.message })
  }
}
